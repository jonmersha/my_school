CREATE TABLE USERS(
    ID INT AUTO_INCREMENT PRIMARY KEY,
    EMAIL VARCHAR(100) UNIQUE,
    FULL_NAME VARCHAR(200),
    USER_ROLE VARCHAR(200),
    PASSWORD VARCHAR(100)
);
CREATE TABLE RESOURCE(
    ID INT AUTO_INCREMENT PRIMARY KEY,
    RESOURCE_TYPE INT,-- [TEXT,VIDEO]
    GRADE INT,
    FILE_PATH VARCHAR(),
    isFREE boolean,
    PRICE double,
    OWNER INT,
);


CREATE TABLE STUDENT_RESOURCE(
	STUDENT_ID INT,
    RESOURCE_ID INT,
    PRIMARY KEY(STUDENT_ID,RESOURCE_ID)
);
